BOMAN_INDEX_SCALE = {
    'A': 0.28, 'R': -0.66, 'N': -0.16, 'D': -0.49, 'C': 0.13,
    'Q': -0.14, 'E': -0.45, 'G': 0.00, 'H': -0.19, 'I': 0.73,
    'L': 0.53, 'K': -0.81, 'M': 0.26, 'F': 0.61, 'P': -0.14,
    'S': -0.01, 'T': 0.05, 'W': 0.37, 'Y': 0.02, 'V': 0.47
}

def calculate_boman_index(sequence):
    return sum(BOMAN_INDEX_SCALE.get(aa.upper(), 0.0) for aa in sequence)

def calculate_net_charge_at_ph74(sequence):
    net_charge = 0.0
    for aa in sequence:
        if aa == 'K' or aa == 'R': net_charge += 1.0
        elif aa == 'D' or aa == 'E': net_charge -= 1.0
        elif aa == 'H': net_charge += 0.1
    return net_charge

