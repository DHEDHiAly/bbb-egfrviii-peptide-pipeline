import os, gc, math, numpy as np, pandas as pd
from Bio.PDB import PDBParser, PDBIO, Select
from scipy.spatial.transform import Rotation

parser = PDBParser(QUIET=True)
peptides = {
    "P1_Linear": "TFFYGGSRGKRNNFKTEGWRGGRL",
    "P2_Capped": "TFFYGGSRGKRNNFKTEGWRGGRL",
    "P3_Cyclized": "TFFYGGSRGKRNNFKTCVPLPHLKFC",
    "P4_RetroEnantio": "TFFYGGSRGKRNNFKTEGWRGGRL"
}
DOCKING_CENTER = np.array([45.0, 62.0, 38.0])

def get_atom_coords(structure):
    return np.array([atom.get_coord() for atom in structure.get_atoms()])

def get_atoms_list(structure):
    return list(structure.get_atoms())

def score_pose(pep_coords, rec_coords):
    score = 0.0
    for pc in pep_coords:
        dists_sq = np.sum((rec_coords - pc)**2, axis=1)
        min_d = np.sqrt(np.min(dists_sq))
        if min_d < 2.2: score += 10.0
        elif min_d < 3.8: score -= 1.5
        elif min_d < 5.0: score -= 0.5
    return score

def dock_peptide(name):
    pep_pdb = f"/content/{name}.pdb"
    rec_pdb = "/content/receptor_clean.pdb"
    if not os.path.exists(pep_pdb) or not os.path.exists(rec_pdb):
        print(f"  Files missing for {name}"); return None, None
    rec_struct = parser.get_structure("rec", rec_pdb)
    pep_struct = parser.get_structure("pep", pep_pdb)
    rec_coords = get_atom_coords(rec_struct)
    pep_coords = get_atom_coords(pep_struct)
    pep_atoms = get_atoms_list(pep_struct)
    pep_coords = pep_coords - pep_coords.mean(axis=0) + DOCKING_CENTER
    best_score = 9999.0
    best_coords = pep_coords.copy()
    np.random.seed(42)
    rotations = Rotation.random(2000)
    for rot in rotations:
        rotated = rot.apply(pep_coords - DOCKING_CENTER) + DOCKING_CENTER
        s = score_pose(rotated, rec_coords)
        if s < best_score:
            best_score = s; best_coords = rotated.copy()
    for i, atom in enumerate(pep_atoms):
        atom.set_coord(best_coords[i])
    out_pdb = f"/content/{name}_docked.pdb"
    io = PDBIO(); io.set_structure(pep_struct); io.save(out_pdb)
    return round(best_score * 0.1, 2), out_pdb

def build_complex(receptor_pdb, peptide_pdb, out_pdb):
    with open(out_pdb,"w") as out:
        with open(receptor_pdb) as f:
            for line in f:
                if line.startswith("ATOM"): out.write(line)
        out.write("TER\n")
        with open(peptide_pdb) as f:
            for line in f:
                if line.startswith("ATOM"): out.write(line[:21]+"B"+line[22:])
        out.write("END\n")

def score_interactions(complex_pdb):
    struct = parser.get_structure("cpx", complex_pdb)
    model = struct[0]
    chains = list(model.get_chains())
    if len(chains) < 2: return 0,0,0,0
    rec_atoms = list(chains[0].get_atoms())
    pep_atoms = list(chains[1].get_atoms())
    hb, hy, sb = 0,0,0
    for pa in pep_atoms:
        pres = pa.get_parent().get_resname()
        for ra in rec_atoms:
            rres = ra.get_parent().get_resname()
            dist = pa - ra
            if dist < 3.5 and pa.element in ('N','O') and ra.element in ('N','O'): hb += 1
            if dist < 4.5 and pa.element == 'C' and ra.element == 'C': hy += 1
            if dist < 4.2:
                if (pres in {'LYS','ARG'} and rres in {'ASP','GLU'}) or (pres in {'ASP','GLU'} and rres in {'LYS','ARG'}): sb += 1
    return hb, hy//12, sb, 0

def estimate_dG(hb, hy, sb):
    dG = -0.4*hb - 0.2*hy - 1.2*sb - 2.5
    Kd = math.exp(dG * 1000.0 / (1.987 * 298.15)) * 1e6
    return round(dG, 2), round(Kd, 2)

docking_rows, prodigy_rows, interact_rows = [], [], []
for name in peptides:
    print(f"Processing {name}...")
    score, docked_pdb = dock_peptide(name)
    if score is None: continue
    complex_pdb = f"/content/{name}_complex.pdb"
    build_complex("/content/receptor_clean.pdb", docked_pdb, complex_pdb)
    hb, hy, sb, pi = score_interactions(complex_pdb)
    dG, Kd_uM = estimate_dG(hb, hy, sb)
    docking_rows.append({"Peptide": name, "Score": score})
    interact_rows.append({"Peptide":name, "HBonds":hb, "Hydrophobic":hy, "SaltBr":sb, "Total":hb+hy+sb})
    prodigy_rows.append({"Peptide":name, "dG":dG, "Kd_uM":Kd_uM})

df_final = pd.DataFrame(docking_rows).merge(pd.DataFrame(prodigy_rows), on="Peptide").merge(pd.DataFrame(interact_rows), on="Peptide")
display(df_final)
df_final.to_csv("/content/Results_Final.csv", index=False)

