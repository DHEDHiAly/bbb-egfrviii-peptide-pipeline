def pdb_to_pdbqt_receptor(pdb_in, pdbqt_out):
    out_lines = []
    if not os.path.exists(pdb_in):
        print(f"Error: Input PDB {pdb_in} not found.")
        return False
    with open(pdb_in) as f:
        for line in f:
            if line.startswith("ATOM") or line.startswith("HETATM"):
                atom_line_prefix = line[:54]
