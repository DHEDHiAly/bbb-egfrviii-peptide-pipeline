def fold_sequence(name, sequence):
    url = "https://api.esmatlas.com/foldSequence/v1/pdb/"
    headers = {"Content-Type": "application/x-www-form-urlencoded"}
    output_filename = f"/content/{name}.pdb"
    print(f"\nFolding {name} ({sequence})...")
    try:
        response = requests.post(url, data=sequence.encode('utf-8'), headers=headers, timeout=180)
        response.raise_for_status()
        with open(output_filename, "w") as f:
            f.write(response.text)
        print(f"{name}: saved to {output_filename}")
        plddt_score = None
        for line in response.text.splitlines():
            if 'remark 3' in line.lower() and 'plddt' in line.lower():
                try:
                    plddt_score = float(line.split('plddt=')[-1].split(' ')[0])
                    print(f"{name}: pLDDT: {plddt_score:.2f}")
                    break
                except ValueError:
                    pass
    except Exception as e:
        print(f"{name}: API call failed: {e}")
    time.sleep(3)

