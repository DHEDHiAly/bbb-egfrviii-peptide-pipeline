    complex_pdb = f"/content/{name}_complex.pdb"
    out_dir = f"/content/plip_{name}"
    os.makedirs(out_dir, exist_ok=True)
    if not os.path.exists(complex_pdb):
        print(f"{name}: complex PDB not found")
        return 0,0,0,0
    try:
        result = subprocess.run(["plip", "-f", complex_pdb, "-o", out_dir, "-x", "--nohydro"],
                                capture_output=True, text=True, timeout=180)
        xml_file = None
        for fname in os.listdir(out_dir):
            if fname.endswith(".xml"):
                xml_file = os.path.join(out_dir, fname); break
        if xml_file and os.path.exists(xml_file):
            tree = ET.parse(xml_file); root = tree.getroot()
            hbonds = len(root.findall(".//hydrogenbond"))
            hydrophob = len(root.findall(".//hydrophobicinteraction"))
            saltbridge = len(root.findall(".//saltbridge"))
            pistack = len(root.findall(".//pistacking"))
            return hbonds, hydrophob, saltbridge, pistack
    except Exception as e:
        print(f"  {name} PLIP error: {e}")
    return 0,0,0,0

